            <h6><b>Fill up on folic acid</b></h6>There's no doubt that your body works overtime when you're expecting — so
            help give it the extra nutrients it needs to baby-build by getting 400 to 600 micrograms of folic acid daily
            from all sources, including your prenatal vitamin and folate-rich foods.
            <br> Folic acid (and its natural form, folate) can help reduce the risk of birth defects including congenital
            heart and neural tube defects in your baby, and has even been shown to lower your chances of gestational
            diabetes, preterm labor and miscarriage.</br>

            <br></br>
            <h6><b>Seek Medical Advice Regarding Medications</b></h6>If you’re taking a prescription, OTC or herbal med, ask
