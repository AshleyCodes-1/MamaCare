            the follicles to mature, some faster than others.
            <br>A second hormone, lutenizing hormone (LH), increases around day 5 and also works with FSH to stimulate the
            follicles. Each follicle contains an egg, and each month only one follicle becomes the dominant one, destined
            for ovulation.</br>
            <br> As the follicles mature, they produce another hormone, estrogen, which does two things. First, it
            encourages the lining of the uterus to begin thickening again. Second, once a high enough level of estrogen is
            reached, it will trigger a spike in the production of LH.</br>
            <br> Believe it or not, the countdown to delivery day begins now, during the period before fertilization — even
            though egg and sperm haven't even been in the same room (or womb!).

